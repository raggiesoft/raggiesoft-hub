            <div class="card border-0 shadow-lg bg-body-tertiary narrative-card mb-5">
                <div class="card-body p-5 text-body-secondary">
                    
                    <span class="timestamp">Sep 13, 2018 // 08:30 AM PST // Valley Regional Airport (BUR)</span>
                    <h5 class="text-primary fw-bold text-uppercase mb-4">The Landing</h5>

                    <p>
                        The tires of the Gateway Airlines Boeing 737 touched down with a chirp of burning rubber.
                    </p>
                    <p>
                        Tyler Wright checked his watch. "Wheels down. 08:30 local. We made up time over the Rockies."
                    </p>
                    <p>
                        It had been a brutal logistical sprint. The "Smoking Gun" letter had arrived in Blacksburg yesterday afternoon. By 6:00 PM, Holly had booked five last-minute First Class seats from <strong>Roanoke (ROA)</strong> to <strong>Burbank (BUR)</strong>, connecting through the Atlanta hub.
                    </p>
                    <p>
                        "Remind me why we're doing this?" Evan asked, stretching his massive frame into the aisle as the seatbelt sign pinged off. "We could have just shredded the letter."
                    </p>
                    <p>
                        Holly O'Connell didn't look up from her phone. She was calculating the Uber surge pricing in Los Angeles.
                    </p>
                    <p>
                        "Because he owes me fifty cents," Holly said. "He sent a hostile takeover threat using a bulk mail permit on a single envelope. The mail carrier made me pay the postage due."
                    </p>
                    <p>
                        She stood up, smoothing her blazer.
                    </p>
                    <p>
                        "I don't care about the $150 million offer. I care about the disrespect. We are going to walk into his office, look him in the eye, and make him give me two quarters."
                    </p>

                    <div class="alert alert-dark border-start border-4 border-warning bg-body-secondary py-3 my-4 small">
                        <div class="d-flex align-items-center">
                            <i class="fa-solid fa-file-invoice-dollar text-warning fs-3 me-3"></i>
                            <div>
                                <strong>Expense Report:</strong><br>
                                Airfare (Last Minute / First Class): <strong>$18,450.00</strong><br>
                                Hotel (The Metropolitan / 2 Weeks): <strong>$8,200.00</strong><br>
                                <strong>Target Recovery:</strong> <span class="text-danger fw-bold">$0.50</span>
                            </div>
                        </div>
                    </div>

                    <p>
                        In the window seat (1A), Ryan O'Connell looked out at the sun-bleached tarmac. For him, the First Class ticket wasn't a luxury; it was a medical necessity. The extra legroom allowed him to manage the spasms in his paralyzed legs during the five-hour flight from Atlanta.
                    </p>
                    <p>
                        "Systems check," Ryan murmured. He shifted his weight, testing his balance. "Spasticity is manageable. Hydration is nominal."
                    </p>
                    <p>
                        Cassidy sat in 1B. She adjusted her glasses, her leg bouncing slightly—a nervous tic common with her cerebral palsy. "Are we tourists yet?" she whispered.
                    </p>
                    <p>
                        "Not until we get the quarters," Ryan replied.
                    </p>

                    <hr class="border-secondary opacity-25 my-5">

                    <h5 class="text-primary fw-bold text-uppercase mb-4">Protocol: The Tarmac Transfer</h5>
                    
                    <p>
                        Valley Regional (BUR) was their airport of choice for one specific reason: <strong>Stairs, not Jetways.</strong>
                    </p>
                    <p>
                        Gateway Airlines ground crew brought the aisle chair to the aircraft door, but Ryan waved them off. He saw his custom <strong>Carbon-Fiber Veloce</strong> wheelchair waiting at the bottom of the air-stairs.
                    </p>
                    <p>
                        "Evan," Ryan said.
                    </p>
                    <p>
                        Evan didn't need instructions. He stepped up, scooped his brother into a fireman's carry with effortless practice, and carried him down the stairs to the tarmac. Tyler followed with the cushion.
                    </p>
                    <p>
                        Ryan transferred into his chair on the hot pavement. He clicked the seat lock. He pushed off the asphalt, spinning the chair around.
                    </p>
                    <p>
                        "Solid ground," Ryan said. "Let's go find our ride."
                    </p>
                </div>
            </div>

            <div class="card wall-card shadow-lg mb-5">
                <div class="card-body p-5">
                    <h5 class="text-primary fw-bold text-uppercase mb-4">The Tarmac</h5>
                    
                    <p>
                        Valley Regional was Ryan’s favorite airport for one specific reason: No Jetways.
                    </p>
                    <p>
                        Ryan transferred from the aisle chair to his custom Carbon-Fiber Veloce at the aircraft door. He felt the familiar click of his seat lock engaging. He pushed off the bulkhead, caught the rims of his wheels, and rolled down the switchback ramp into the warm California morning.
                    </p>
                    <p>
                        "Temperature is rising," Holly noted, stepping onto the tarmac behind him. "It’s going to be ninety degrees by noon. Hydrate."
                    </p>
                    <p>
                        Ryan grabbed the water bottle from the side pouch of his chair without breaking his pushing rhythm. "Way ahead of you."
                    </p>
                    
                    <div class="alert alert-dark border-start border-4 border-info bg-body-secondary py-3 my-4">
                        <div class="d-flex align-items-center">
                            <i class="fa-duotone fa-wheelchair-move text-info fs-3 me-3"></i>
                            <div class="small text-body-emphasis fst-italic">
                                <strong>Formation Protocol:</strong> Cassidy moved to his right. She didn't walk beside him; she walked with him. Her left hand rested firmly on the push-handle of his wheelchair. She didn't push him—Ryan didn't need pushing—but she used the sturdy, carbon-fiber frame as a rolling walker. It was a symbiotic machine.
                            </div>
                        </div>
                    </div>

                    <p>
                        They moved as a phalanx through the outdoor baggage claim. Tyler and Evan flanked the group, grabbing the five Titan Cases containing their "tourist gear" (and the suits for tomorrow) from the carousel.
                    </p>
                    <p>
                        They approached the exit. A line of exhausted travelers was queuing up at the rental kiosks.
                    </p>
                    <p>
                        "We should have just told them 'No' on the phone," Ryan said, scanning the curb. "We're here to see the Space Shuttle Horizon, not a boardroom."
                    </p>
                    <p>
                        "We're here to do both," Holly corrected. "We take the meeting because they're paying for the hotel. We reject the offer because they're idiots. Then we spend three days eating tacos. That is efficiency."
                    </p>

                </div>
            </div>

            <div class="card wall-card shadow-lg mb-5">
                <div class="card-body p-5">
                    <div class="d-flex align-items-center mb-4">
                        <i class="fa-duotone fa-hand-palm text-danger fs-2 me-3"></i>
                        <h5 class="text-white text-uppercase letter-spacing-2 mb-0">The Interception</h5>
                    </div>

                    <p class="text-light">
                        "Mr. Wright? The O'Connell party?"
                    </p>
                    <p class="text-light">
                        A man in a black suit stepped out from behind a concrete pillar. He was holding an mPad. "I'm Marcus," the driver said, flashing a service-industry smile. "Mr. Frost arranged for your ground transport."
                    </p>
                    <p class="text-light">
                        He turned the mPad around to show them the welcome screen. The text on the screen did not say "The Stardust Engine."
                    </p>
                    <p class="text-white fw-bold fs-4 text-center my-4 font-monospace border border-secondary p-3 rounded bg-black">
                        WELCOME MR. & MRS. O'CONNELL
                    </p>
                    <p class="text-light">
                        The air on the curb didn't just drop; it froze. Cassidy flinched. She physically recoiled, her hand jerking off Ryan’s wheelchair handle as if it had burned her.
                    </p>
                    <p class="text-light">
                        "No," Cassidy whispered, her breathing hitching. "No. Not again. I'm not... he's my..."
                    </p>
                    <p class="text-white-50 fst-italic">
                        She looked at Ryan, terror in her eyes. It was 1992 all over again. The photo shoot. The mirror. The clipboard.
                    </p>
                    <p class="text-light">
                        Evan and Tyler Wright stepped forward instantly. They didn't cross their arms this time; they dropped their bags. The thud of the heavy Titan Cases hitting the concrete sounded like a warning shot.
                    </p>
                    <p class="text-light">
                        "Turn that screen off," Tyler growled.
                    </p>
                    <p class="text-light">
                        "I am his sister," Cassidy choked out, her voice trembling with a mix of shame and fury.
                    </p>
                    <p class="text-light">
                        Ryan O'Connell stopped rolling. His expression went from "vacation mode" to "cold dead stare" in a fraction of a second.
                    </p>
                    <p class="text-white fw-bold">
                        "You have exactly five seconds," Ryan said, his voice dangerously quiet, "to delete that file."
                    </p>
                    
                    <hr class="border-secondary opacity-50 my-4">

                    <p class="text-light">
                        Holly O'Connell walked past the twins. She simply plucked the mPad out of the driver's hands. She tapped the screen, deleting the image, and handed the device back.
                    </p>
                    <p class="text-light">
                        "You can tell Mr. Frost that his 'Research Department' just cost him his first strike. And we play three-strike rules."
                    </p>
                </div>
            </div>

