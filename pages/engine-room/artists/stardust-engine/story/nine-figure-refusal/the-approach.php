            </div>

            <div class="card border-0 shadow-lg narrative-card mb-5">
                <div class="card-body p-5 text-body-secondary">
                    
                    <span class="timestamp">Sep 13, 2018 // 10:45 AM PST // The Metropolitan Suites</span>
                    <h5 class="text-primary fw-bold text-uppercase mb-4">Base of Operations</h5>

                    <p>
                        The subway ride from Union Station had been uneventful. They surfaced into the blinding Los Angeles sun and walked two blocks to the hotel. It wasn't the Grand Monarch where Omni-Global had tried to stick them. It was The Metropolitan Suites.
                    </p>
                    <p>
                        Holly walked to the front desk. She didn't slap down a corporate card. She pulled out her own Engine Room Records debit card.
                    </p>
                    <p>
                        "The Monarch," Holly whispered in the elevator, "has a master account controlled by Jameson Frost. The minute we walk out of that meeting tomorrow, he'll cancel the rooms and lock us out."
                    </p>
                    <p>
                        She looked at Ryan.
                    </p>
                    <p>
                        "We own the ground we stand on. That way, no matter what happens in that boardroom, we have a home for two weeks."
                    </p>

                    <hr class="border-secondary opacity-25 my-5">

                    <h5 class="text-primary fw-bold text-uppercase mb-4">Protocol: The Sleeping Arrangement</h5>

                    <p>
                        In the center of the main suite stood a single, massive King-sized bed.
                    </p>
                    <p>
                        To an outsider, seeing three adult siblings sharing a bed might look strange. To The Stardust Engine, it was Protocol.
                    </p>
                    <p>
                        "Middle is clear," Ryan said, transferring from his wheelchair to the bed with a practiced lift-and-swing motion. He scooted to the center, dragging his paralyzed legs into position.
                    </p>
                    
                    <div class="row align-items-center bg-body-secondary rounded p-3 border border-secondary mt-4 mb-4">
                        <div class="col-md-2 text-center">
                            <i class="fa-duotone fa-bed-front text-secondary fs-1 opacity-50"></i>
                        </div>
                        <div class="col-md-10">
                            <p class="small text-body-emphasis font-monospace mb-0">
                                <strong>Safety Protocol:</strong> Because Ryan had no sensation below T10, he couldn't feel where his legs were. In a hotel bed without rails, a violent spasm could throw him onto the floor. By sleeping in the middle, he had Holly on his right and Cassidy on his left. They were his guardrails.
                            </p>
                        </div>
                    </div>

                    <p>
                        "Two weeks," Tyler said, leaning against the doorframe of the connecting room. "What's the play for today?"
                    </p>
                    <p>
                        "Today we rest," Ryan said, his eyes closing. "Tomorrow is business. But right now? We're just tourists."
                    </p>

                </div>
            </div>

            <div class="card border-0 shadow-lg narrative-card mb-5">
                <div class="card-body p-5 text-body-secondary">
                    
                    <span class="timestamp">Sep 14, 2018 // 09:00 AM PST // 2000 Avenue of the Stars</span>
                    <h5 class="text-danger fw-bold text-uppercase mb-4">The Armory</h5>

                    <p>
                        The lobby was designed to intimidate. A cathedral of glass and steel that stretched two full stories up, creating a vast, echoing vacuum of air designed to make human beings feel small.
                    </p>
                    <p>
                        The Stardust Engine did not look small. They moved in formation, wearing their "Uniforms": Midnight Blue polos with the silver <em>Engine Room</em> gear logo.
                    </p>
                    <p>
                        "Target acquired," Ryan said. "Omni-Global Media. Penthouse."
                    </p>
                    <p>
                        "We aren't going there yet," Holly said. "We have a stop to make."
                    </p>
                    <p>
                        They bypassed the main security desk for the restricted elevators. The guard, a serious man named Alvarez with twenty years on the job, stepped forward instantly. He raised a hand, blocking the path.
                    </p>
                    <p>
                        "Excuse me, folks," Alvarez said firmly. "Restricted elevators. I need you to step back to the Visitor Desk."
                    </p>
                    <p>
                        Holly didn't argue. She didn't explain. She simply reached into her pocket.
                    </p>
                    <p>
                        She pulled out a heavy, black RFID keycard with a silver holographic column and held it to the reader. 
                    </p>
                    <p>
                        <em>BEEP.</em>
                    </p>
                    <p>
                        Alvarez glanced at his console monitor. His eyebrows shot up. <strong class="text-success text-uppercase">TIER 1: AUTHORIZED AGENT.</strong>
                    </p>
                    <p>
                        "Apologies, ma'am," he said, his tone shifting from enforcer to professional deference. "But I need all parties to badge in. No tailgating. Building policy."
                    </p>
                    <p>
                        Holly smiled. It was a genuine smile. "I appreciate that, Officer. Good protocol."
                    </p>
                    <p>
                        She stepped through the turnstile. Behind her, Ryan rolled forward. He tapped a card attached to a retractable reel on his hip.
                    </p>
                    <p>
                        <em>BEEP.</em> <strong class="text-success text-uppercase small">TIER 1: AUTHORIZED AGENT.</strong>
                    </p>
                    <p>
                        Then Cassidy.
                    </p>
                    <p>
                        <em>BEEP.</em>
                    </p>
                    <p>
                        Then Evan. Then Tyler.
                    </p>
                    <p>
                        <em>BEEP. BEEP.</em>
                    </p>
                    <p>
                        Officer Alvarez stared at the screen. Five Tier 1 badges. Five "Owners." He looked back at the group in the matching polo shirts. They weren't tourists. They were an invasion force.
                    </p>
                    <p>
                        "Elevator C is priority-locked for you," Alvarez said, stepping aside and holding his arm out to clear the path. "Have a good morning, folks."
                    </p>
                    
                    <div class="alert alert-secondary border-0 small fst-italic my-3">
                        <i class="fa-solid fa-elevator me-2"></i><strong>Security Note:</strong> The elevator button panel skipped "2" entirely. The "Phantom Second Floor" was a mechanical mezzanine, accessible only via freight elevators in the rear corridor. 
                        <br><br>
                        Holly's RFID badge unlocked the hidden service menu. If she wanted to, she could take her family into the guts of the building. Instead, she pressed <strong>3</strong>.
                    </div>

                    <hr class="border-secondary opacity-25 my-4">

                    <p>
                        The elevator doors opened onto the <strong>Third Floor Mezzanine</strong>.
                    </p>
                    <p>
                        It was the only suite on the level. The reception desk for <strong>Pacific Rim Properties (Suite 300)</strong> didn't face a blank wall; it faced a sweeping glass balcony rail that looked directly down into the main atrium.
                    </p>
                    <p>
                        From her desk, Elena—the on-site manager—could see everyone who entered the building. The Landlord wasn't just in the tower; the Landlord was watching the door.
                    </p>
                    
                    <p>
                        "Welcome to the West Coast office," Elena said, standing up. She handed Holly a single, heavy document folder. "Printed on the 32lb linen stock, silver embossed. Just like you asked."
                    </p>
                    <p>
                        Holly opened the folder. She ran her finger over the textured paper. It made a sharp <em>snap</em>. It was the Eviction Notice.
                    </p>
                    <p>
                        "Perfect," Holly said, sliding it into her briefcase. "And the network?"
                    </p>
                    <p>
                        "Whitelisted on the Building Management VLAN. You have Tier 1 priority."
                    </p>
                    <p>
                        "Good. If Jameson Frost wants a presentation, I’m going to give him one."
                    </p>

                </div>
            </div>

            <div class="card border-danger shadow-lg mb-5">
                <div class="card-body p-5">
                    
                    <h3 class="h4 fw-bold text-uppercase mb-4 text-secondary">The Lobby</h3>
                    
                    <p class="lead">
                        The elevator doors opened onto the 40th floor, revealing a lobby that smelled of ozone and intimidation. The carpet was too thick. The lighting was too low. The silence was engineered.
                    </p>
                    <p>
                        Holly O'Connell stepped out first, her heels sinking slightly into the plush wool. She adjusted her blazer, checking the alignment of her enamel pin. Behind her, the twins—Evan and Tyler—flanked the door like Secret Service agents, minus the earpieces and plus twenty years of roadie muscle.
                    </p>
                    <p>
                        Cassidy stayed close to the wall, her eyes scanning the exits.
                    </p>
                    <p>
                        Ryan rolled out last. His custom titanium rig hummed softly on the marble threshold before hitting the carpet. He hated thick carpet. It was like pushing through mud. He gave his rims a hard shove, propelling himself forward with a grunt of irritation.
                    </p>

                    <hr class="my-5 opacity-25">

                    <h4 class="h5 fw-bold text-uppercase mb-3">The Reception Desk</h4>
                    
                    <p>
                        The reception desk was a massive slab of polished obsidian, designed to look like it was floating. It was comically high—clearly built to make the person behind it look down on you.
                    </p>
                    <p>
                        "Check in," Holly whispered. "I'll handle the coats."
                    </p>
                    <p>
                        Ryan rolled up to the desk. Because of the height, he was staring directly at the wood grain of the paneling. He couldn't see the person behind it.
                    </p>
                    <p>
                        "Excuse me," Ryan said, pitching his voice to cut through the ambient drone of the AC.
                    </p>
                    <p>
                        A head popped up.
                    </p>
                    <p>
                        She was young. Painfully young. Maybe nineteen. She had dark hair pulled back into a severe bun that looked like it was giving her a headache. She was wearing a crisp white blouse that looked stiff and uncomfortable.
                    </p>
                    <p>
                        "Oh!" she squeaked, her eyes widening as she looked down at him. "I'm sorry, I didn't... I didn't see you down there."
                    </p>
                    <p>
                        She scrambled to stand up. Or, she tried to.
                    </p>
                    <p>
                        Ryan watched the mechanics of her movement. He saw her hands grip the edge of the desk, knuckles white. He saw the way her shoulders hunched, taking the weight off her lower body. He heard the distinct <em>clack-scrape</em> of a footplate shifting under the desk.
                    </p>
                    <p>
                        She wasn't standing up. She was repositioning.
                    </p>

                    <div class="ryan-observation">
                        <h5 class="mb-2">Ryan's Log</h5>
                        <p class="mb-0">
                            I saw the chair. It was a standard-issue hospital clunker, probably tucked under the desk where no one could see it.
                        </p>
                        <p class="mb-0 mt-2">
                            But it wasn't the chair that pissed me off. It was the shoes.
                        </p>
                        <p class="mb-0 mt-2">
                            She was wearing black patent leather pumps. Three-inch heels. Pointy toes. I saw them when she shifted—her feet were strapped onto the footplates, the heels hooked precariously over the back edge to keep them from slipping. Her ankles were swollen.
                        </p>
                        <p class="mb-0 mt-2">
                            She's paralyzed. Maybe incomplete, maybe ambulatory, but she's a wheelie. And she's wearing <em>heels</em>.
                        </p>
                        <p class="mt-2 text-danger fw-bold">
                            She's hiding it. She thinks she has to.
                        </p>
                    </div>

                    <p>
                        "I'm Jessica," she said, her voice trembling. She smoothed the front of her blouse, trying to look professional, trying to look like she wasn't currently in agony. "Welcome to Omni-Global. Do you... do you have an appointment?"
                    </p>
                    <p>
                        Ryan didn't answer immediately. He just looked at her. He looked at the high desk that cut her off at the neck. He looked at the thick carpet that would make it impossible for her to push that heavy hospital chair to the bathroom without help.
                    </p>
                    <p>
                        He looked at the fear in her eyes. It was the fear of an intern who hadn't been paid in three months and was terrified that her disability would be the reason she was let go.
                    </p>
                    <p>
                        "Yeah," Ryan said, his voice softer than usual. "We're here to see Jameson Frost."
                    </p>
                    <p>
                        "Mr. Frost is in the boardroom," Jessica said, typing frantically. "I'll... I'll let him know you're here."
                    </p>
                    <p>
                        Ryan turned his chair around to face his family. Holly was watching him. She had seen him clock the receptionist. She raised an eyebrow.
                    </p>
                    <p>
                        <em>We're taking her,</em> Ryan signaled with a subtle nod.
                    </p>
                    <p>
                        Holly nodded back. <em>We're taking her.</em>
                    </p>

