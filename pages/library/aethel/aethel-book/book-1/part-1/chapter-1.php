                He turned back to his charts. The room was filled with measuring instruments of his own design—brass astrolabes, crystal lenses, and mounds of parchment covered in complex arithmancy. For twenty years, he had served as the King's High Sage. For twenty years, he had tried to predict the seasons, the tides, and the surges of wild magic that plagued the kingdom.
            </p>

            <p>
                And for twenty years, he had failed. Not because his math was wrong, but because the variables refused to behave.
            </p>

            <p>
                "Emotion," he muttered, tracing the erratic orbit of the Golden Sun on his chart. "It burns without purpose. It gives, then it withholds. It is a child throwing a tantrum in the heavens." He moved his finger to the Silver Sun's path. "And you... you are no better. Cold logic without the will to act. You watch the... <em>disorder</em> and do nothing to stop it."
            </p>

            <p>
                A knock at the heavy oak door broke his concentration. "Enter," he snapped.
            </p>

            <p>
                It was a junior scribe, trembling, holding a sealed scroll bearing the King's seal. "My Lord Malakor... the Council is assembled. They await your report on the harvest predictions."
            </p>

            <p>
                Malakor stared at the scroll. The harvest. Another year of guessing. Another year of praying that Sol-Aura wouldn't scorch the crops in a fit of passion, or that Lun-Argent wouldn't freeze the roots in a fit of melancholy.
            </p>

            <p>
                "Tell them I am coming," Malakor said, his voice smooth and dangerous. "And tell them... I have a new proposal. One that will solve the harvest problem. Permanently."
            </p>

