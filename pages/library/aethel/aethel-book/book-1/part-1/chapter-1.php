                As the scribe scurried away, Malakor looked back at the sky one last time. He didn't see a binary star system. He saw a mistake. A mistake that required a correction. He picked up a quill and dipped it into a pot of ink that was darker than the night sky. On a fresh sheet of parchment, he drew a single, perfect circle. Not two suns fighting for dominance. One.
            </p>

            <p>
                "Order," he breathed. "It is time for Order."
            </p>

