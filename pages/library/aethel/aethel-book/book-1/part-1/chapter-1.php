            </p>

            <p>
                To the common folk in the streets below, it was a beautiful day. The Golden Sun, <strong>Sol-Aura</strong>, was high and warm, baking the cobblestones. The Silver Sun, <strong>Lun-Argent</strong>, was a pale crescent chasing it, lending a pearlescent shimmer to the clouds. They cheered for the warmth. They prayed to the Silver for wisdom. They reveled in the unpredictability of it all.
            </p>

            <p>
                Malakor despised it. "Inefficient," he whispered, the word tasting like ash.
            </p>

